/** 기존 DB 레시피 가격 자동 설정용 맵 */
export function getSeedPriceMap() {
  const map = new Map()
  for (const r of getSeedRecipes()) {
    map.set(`${r.name}__${String(r.temperature)}`, r.price)
  }
  return map
}
